void filterhuruf(KeyEvent a){
        if(Character.isAlphabetic(a.getKeyChar())){
            a.consume();
            JOptionPane.showMessageDialog(null,"Silahkan Masukan Dengan Karakter Angka (No.Telpon)","kesalahan",JOptionPane.WARNING_MESSAGE);
        }
    }
void filterangka(KeyEvent b){
if(Character.isDigit(b.getKeyChar())){
            b.consume();
            JOptionPane.showMessageDialog(null,"Silahkan Masukan Dengan Karakter Huruf (Nama)","kesalahan",JOptionPane.WARNING_MESSAGE);
        }    
}
