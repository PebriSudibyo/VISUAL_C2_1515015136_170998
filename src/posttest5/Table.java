    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        // TODO add your handling code here:
        Object[] data = new Object[3];
        data[0] = tfnama.getText();
        data[1] = tfnomor.getText();
        data[2] = cbxkota.getSelectedItem();
        model.addRow(data);

        bupdate.setEnabled(true);
        bhapus.setEnabled(true);
        bulang.setEnabled(true);
    }//GEN-LAST:event_bsimpanActionPerformed

    private void bupdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bupdateMouseClicked
        // TODO add your handling code here:
        try{
            int baris = jTable1.getSelectedRow();

            jTable1.setValueAt(tfnama.getText(), baris, 0);
            jTable1.setValueAt(tfnomor.getText(), baris, 1);
            jTable1.setValueAt(cbxkota.getSelectedItem(), baris, 2);
        }
        catch(ArrayIndexOutOfBoundsException e){
            JOptionPane.showMessageDialog(this,"table belum dipilih","kesalahan",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_bupdateMouseClicked

